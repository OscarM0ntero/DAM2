#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (!lst || !f)
		return ;
	f(lst->content);
	tmp = lst;
	while (tmp->next != NULL)
	{
		tmp = tmp->next;
		f(tmp->content);
	}
}
